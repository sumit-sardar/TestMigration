package utils;

public class GridDropLists {
	
	private String[] gradeOptions;
	private String[] testCatalogOptions;
	private String[] contentAreaOptions;
	private String[] formOptions;

	/**
	 * @return the gradeOptions
	 */
	public String[] getGradeOptions() {
		return gradeOptions;
	}

	/**
	 * @param gradeOptions the gradeOptions to set
	 */
	public void setGradeOptions(String[] gradeOptions) {
		this.gradeOptions = gradeOptions;
	}

	/**
	 * @return the testCatalogOptions
	 */
	public String[] getTestCatalogOptions() {
		return testCatalogOptions;
	}

	/**
	 * @param testCatalogOptions the testCatalogOptions to set
	 */
	public void setTestCatalogOptions(String[] testCatalogOptions) {
		this.testCatalogOptions = testCatalogOptions;
	}

	/**
	 * @return the contentAreaOptions
	 */
	public String[] getContentAreaOptions() {
		return contentAreaOptions;
	}

	/**
	 * @param contentAreaOptions the contentAreaOptions to set
	 */
	public void setContentAreaOptions(String[] contentAreaOptions) {
		this.contentAreaOptions = contentAreaOptions;
	}

	/**
	 * @return the formOptions
	 */
	public String[] getFormOptions() {
		return formOptions;
	}
	
	/**
	 * @param formOptions the formOptions to set
	 */
	public void setFormOptions(String[] formOptions) {
		this.formOptions = formOptions;
	}

}
