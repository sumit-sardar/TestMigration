package com.ctb.tms.bean.login.response.testingSessionData.accomodations.stereotypeStyle; 

public class StereotypeStyle 
{ 
    private String stereotype;
    private String bgColor;
    private String fontColor;
    private float fontMagnification;
    
    /**
	 * @return Returns the bgColor.
	 */
	public String getBgColor() {
		return bgColor;
	}
	/**
	 * @param bgColor The bgColor to set.
	 */
	public void setBgColor(String bgColor) {
		this.bgColor = bgColor;
	}
	/**
	 * @return Returns the fontColor.
	 */
	public String getFontColor() {
		return fontColor;
	}
	/**
	 * @param fontColor The fontColor to set.
	 */
	public void setFontColor(String fontColor) {
		this.fontColor = fontColor;
	}
	/**
	 * @return Returns the fontMagnification.
	 */
	public float getFontMagnification() {
		return fontMagnification;
	}
	/**
	 * @param fontMagnification The fontMagnification to set.
	 */
	public void setFontMagnification(float fontMagnification) {
		this.fontMagnification = fontMagnification;
	}
	/**
	 * @return Returns the stereotype.
	 */
	public String getStereotype() {
		return stereotype;
	}
	/**
	 * @param stereotype The stereotype to set.
	 */
	public void setStereotype(String stereotype) {
		this.stereotype = stereotype;
	}
} 
