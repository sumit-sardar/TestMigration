
CREATE SEQUENCE  "OAS"."SEQ_WS_ERROR_LOG_KEY"  MINVALUE 1000 MAXVALUE 999999999999999999999999999 INCREMENT BY 1 START WITH 1000 CACHE 20 NOORDER  NOCYCLE ;

CREATE TABLE WS_ERROR_LOG 
( WS_ERROR_LOG_KEY NUMBER,
  STUDENT_ID NUMBER,
  ROSTER_ID NUMBER,
  SESSION_ID NUMBER,
  STATUS VARCHAR2(15),
  INVOKE_COUNT NUMBER,
  WS_TYPE VARCHAR2(15),
  ADDITIONAL_INFO		VARCHAR2 (250),
  MESSAGE VARCHAR2(4000),
  CREATED_DATE	DATE DEFAULT (sysdate),
  UPDATED_DATE	DATE DEFAULT (sysdate)
);

	 	 
