package comm.ctb.bean;

public class ReadDetailsVO {
	
	private Integer readNumber;
	private String scoreValue;
	private Integer readerId;
	private String dataTime;
	private Integer elapsedTime;
	/**
	 * @return the readNumber
	 */
	public Integer getReadNumber() {
		return readNumber;
	}
	/**
	 * @param readNumber the readNumber to set
	 */
	public void setReadNumber(Integer readNumber) {
		this.readNumber = readNumber;
	}
	/**
	 * @return the scoreValue
	 */
	public String getScoreValue() {
		return scoreValue;
	}
	/**
	 * @param scoreValue the scoreValue to set
	 */
	public void setScoreValue(String scoreValue) {
		this.scoreValue = scoreValue;
	}
	/**
	 * @return the readerId
	 */
	public Integer getReaderId() {
		return readerId;
	}
	/**
	 * @param readerId the readerId to set
	 */
	public void setReaderId(Integer readerId) {
		this.readerId = readerId;
	}
	/**
	 * @return the dataTime
	 */
	public String getDataTime() {
		return dataTime;
	}
	/**
	 * @param dataTime the dataTime to set
	 */
	public void setDataTime(String dataTime) {
		this.dataTime = dataTime;
	}
	/**
	 * @return the elapsedTime
	 */
	public Integer getElapsedTime() {
		return elapsedTime;
	}
	/**
	 * @param elapsedTime the elapsedTime to set
	 */
	public void setElapsedTime(Integer elapsedTime) {
		this.elapsedTime = elapsedTime;
	}
	
	
}
