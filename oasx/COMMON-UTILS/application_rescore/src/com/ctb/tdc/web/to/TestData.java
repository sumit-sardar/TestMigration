package com.ctb.tdc.web.to;

public class TestData {
	
	private int testRosterId;
	private int itemSetId;
	private Double abilityScore;
	private Double sem;
	private String objScore;
	
	public int getTestRosterId() {
		return testRosterId;
	}
	public void setTestRosterId(int testRosterId) {
		this.testRosterId = testRosterId;
	}
	public int getItemSetId() {
		return itemSetId;
	}
	public void setItemSetId(int itemSetId) {
		this.itemSetId = itemSetId;
	}
	public Double getAbilityScore() {
		return abilityScore;
	}
	public void setAbilityScore(Double abilityScore) {
		this.abilityScore = abilityScore;
	}
	public Double getSem() {
		return sem;
	}
	public void setSem(Double sem) {
		this.sem = sem;
	}
	public String getObjScore() {
		return objScore;
	}
	public void setObjScore(String objScore) {
		this.objScore = objScore;
	}

}
