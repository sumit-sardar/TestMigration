package com.ctb.bean;

public class SiteSurvey {
	private Long siteSurveyId ;
	private String siteId;
	private String siteName;
	private String siteType;
	private String sitePath;
	/**
	 * @return the siteSurveyId
	 */
	public Long getSiteSurveyId() {
		return siteSurveyId;
	}
	/**
	 * @param siteSurveyId the siteSurveyId to set
	 */
	public void setSiteSurveyId(Long siteSurveyId) {
		this.siteSurveyId = siteSurveyId;
	}
	/**
	 * @return the siteId
	 */
	public String getSiteId() {
		return siteId;
	}
	/**
	 * @param siteId the siteId to set
	 */
	public void setSiteId(String siteId) {
		this.siteId = siteId;
	}
	/**
	 * @return the siteName
	 */
	public String getSiteName() {
		return siteName;
	}
	/**
	 * @param siteName the siteName to set
	 */
	public void setSiteName(String siteName) {
		this.siteName = siteName;
	}
	/**
	 * @return the siteType
	 */
	public String getSiteType() {
		return siteType;
	}
	/**
	 * @param siteType the siteType to set
	 */
	public void setSiteType(String siteType) {
		this.siteType = siteType;
	}
	/**
	 * @return the sitePath
	 */
	public String getSitePath() {
		return sitePath;
	}
	/**
	 * @param sitePath the sitePath to set
	 */
	public void setSitePath(String sitePath) {
		this.sitePath = sitePath;
	}
	
}
