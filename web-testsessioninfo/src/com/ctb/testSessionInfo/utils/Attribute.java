package com.ctb.testSessionInfo.utils;

public class Attribute {
		
		private String id;
		private String cid;
		private String tcl;

			

		public String getId() {
			return id;
		}

		public void setId(String id) {
			this.id = id;
		}

		/**
		 * @return the cid
		 */
		public String getCid() {
			return cid;
		}

		/**
		 * @param cid the cid to set
		 */
		public void setCid(String cid) {
			this.cid = cid;
		}

		/**
		 * @return the tcl
		 */
		public String getTcl() {
			return tcl;
		}

		/**
		 * @param tcl the tcl to set
		 */
		public void setTcl(String tcl) {
			this.tcl = tcl;
		}



	}
