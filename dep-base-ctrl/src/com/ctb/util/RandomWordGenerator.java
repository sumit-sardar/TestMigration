    public final static class Dictionary {
        public static final String FOURCHAR_LATIN_DICTIONARY = "FourCharWordList";
        public static final String US_DICTIONARY             = "USwordList";

        /**
         * Checks if the input value is valid.
         * @param dictionary_ value to be validated.
         * @return <code>true</code> if param was valid, <code>false</code> otherwise.
         */
        private static boolean isValid( String dictionary_ ) {
            if ((dictionary_.equals( Dictionary.FOURCHAR_LATIN_DICTIONARY )) ||
                (dictionary_.equals( Dictionary.US_DICTIONARY )) )
            {
                return true;
            }else{
                return false;
            }
        }

        /**
         * Checks if the input value is valid.
         * @param dictionary_ value to be validated.
         * @return param if it is valid; otherwise throws exception.
         * @throws CTBSystemException if param is invalid (not on enumeration list)
         */
        private static String validate( String dictionary_ ) throws CTBBusinessException
        {
            if (isValid( dictionary_ )) {
               return ( dictionary_ );
            }else{
               throw new CTBBusinessException( "Invalid value attempt in RandomWordFactory.Dictionary" );
            }
        }
    }

    //properties
    private String   dictionary  = null;
    private String[] vulgarArr   = null;
    private String[] negativeArr = null;


    //ctor
    public RandomWordGenerator( String dictionary_ )
    {
        try{
            this.dictionary = Dictionary.validate( dictionary_ );
            if (dictionary.equals( Dictionary.US_DICTIONARY )) {
                this.vulgarArr   = USwordListVolume1.VULGAR; //since it is the same for all Volumes!
                this.negativeArr = USwordListVolume1.NEGATIVE;
            }else{
                this.vulgarArr   = FourCharWordList.VULGAR;
                this.negativeArr = FourCharWordList.NEGATIVE;
            }
        }
        catch (Exception e){
        }
    }


    /**
     * Generated word FORMAT:<br>
     *     -Random selected word,<br>
     *     -Padded by digits to the derired output string length
     * @param desiredLength_
     * @return randomly chosen dictionary word
     */
    public String generateWordPaddedWithDigits( int desiredLength_ )
    {

        final int    MAX_VOLUMES = 4; //like "volumes" of USwordsList
        final String NINES       = "999999999999999999999999"; //used for the "padding" algorithm

        try{
            Random   rnd           = new Random(System.currentTimeMillis());
            String   generatedWord = null;
            String[] wordArray     = null;

            if (dictionary.equals( Dictionary.US_DICTIONARY )) {
                int i = rnd.nextInt( MAX_VOLUMES );
                switch (i) {
                    case 0:
                        wordArray = volume1.WORDS;
                        break;
                    case 1:
                        wordArray = volume2.WORDS;
                        break;
                    case 2:
                        wordArray = volume3.WORDS;
                        break;
                    case 3:
                        wordArray = volume4.WORDS;
                        break;
                }
            }else{
                wordArray = FourCharWordList.LATIN_DICTIONARY;
            }

            boolean validWord = false;
            while (!validWord) {
                int index = Math.abs( rnd.nextInt() );
                if(index!=0)
                {
                    index = index % (wordArray.length); //make sure index will not be out of bounds for words array
                }
                generatedWord = wordArray[index];
                if (generatedWord.length() < desiredLength_) {  //not equal, as to allow for at least one digit to be padded
                   validWord     = verifyNoVulgar( generatedWord ); //right size, therfore check word "content" correctness
                }
                //else -->continue generating words, until shorter word then desiredLength_ is obtained
            }

            int paddingLength = desiredLength_ - generatedWord.length();
            if (paddingLength>1) {
                String sMagnitude  = NINES.substring(0, paddingLength); //get as many 9's as will be needed for padding
                int    iMagnitude  = Integer.parseInt( sMagnitude); //convert to int primitive
                int    rndDigit    = Math.abs( rnd.nextInt() ); //avoid negative integers
                int    sizedDigits = rndDigit % iMagnitude; //modulo 9's (as many as digits/positions needed)

                generatedWord = generatedWord + sizedDigits; //append digits so the result is of the desired length
            }
            else
            {
               int rndDigit  = Math.abs( rnd.nextInt( 9 )); //to get a least one digit
               generatedWord = generatedWord + rndDigit; //append digits so the result is of the desired length
            }
            return generatedWord;
        }
        catch( Exception e )
        {
        }
        finally{
        }
        return null; //for the Exception "case"...
    }



    /**
     * This checker can be used to verify that any string does not contain a vulgar word.<br>
     * List is included here instead of in CTBConstants so as to offend as few developers as possible.
     *
     * @param stringToCheck_
     * @return True if no vulgar words have been used; <code>False</code> otherwise.
     */
    public boolean verifyNoVulgar( String stringToCheck_ )
    {
        String stringToCheckUpper = stringToCheck_.toUpperCase();

        for (int i = 0; i < this.vulgarArr.length; i++)
        {
            if (stringToCheckUpper.indexOf(this.vulgarArr[i]) != -1 ){
                return false;
            }
        }
        return true;
    }


    /**
     * This checker can be used to verify that any string does not contain a negative word.
     *
     * @param stringToCheck_
     * @return True if no negative words have been used; <code>False</code> otherwise.
     */
    public boolean verifyNotNegative( String stringToCheck_ )
    {
        String stringToCheckUpper = stringToCheck_.toUpperCase();

        for (int i = 0; i < this.negativeArr.length; i++)
        {
            if (stringToCheckUpper.indexOf(this.negativeArr[i]) != -1 ){
                return false;
            }
        }
        return true;
    }

